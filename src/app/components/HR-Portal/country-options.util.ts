export function findCountryOption(value: string): CountryOption | undefined {
  const normalized = value.trim().toLowerCase();
  if (!normalized) {
    return undefined;
  }

  return COUNTRY_OPTIONS.find((option) => {
    const display = `${option.code} - ${option.name}`.toLowerCase();
    return (
      option.code.toLowerCase() === normalized ||
      option.name.toLowerCase() === normalized ||
      display === normalized
    );
  });
}

export function matchCountryOption(value: string): CountryOption | undefined {
  const direct = findCountryOption(value);
  if (direct) {
    return direct;
  }

  const normalized = value.trim();
  const codePrefix = normalized.match(/^([A-Za-z]{2})\s*[-–]/);
  if (!codePrefix) {
    return undefined;
  }

  return COUNTRY_OPTIONS.find((option) => option.code.toLowerCase() === codePrefix[1].toLowerCase());
}

export function formatCountryDisplay(value: string): string {
  const matched = findCountryOption(value);
  return matched ? `${matched.code} - ${matched.name}` : value;
}
